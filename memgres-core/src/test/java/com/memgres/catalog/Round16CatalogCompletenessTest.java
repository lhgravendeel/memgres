    // =========================================================================
    // J12. pg_index.indoption and indkey type
    // =========================================================================

    @Test
    void pg_index_indoption_encodes_desc_and_nulls_order() throws SQLException {
        exec("DROP TABLE IF EXISTS r16_iopt");
        exec("CREATE TABLE r16_iopt (a int, b int)");
        exec("CREATE INDEX r16_iopt_idx ON r16_iopt (a DESC NULLS LAST, b ASC NULLS FIRST)");
        // indoption encoding: 1 (DESC) + 2 (NULLS FIRST); per column
        // a DESC NULLS LAST = 1; b ASC NULLS FIRST = 2
        String opt = str("SELECT indoption::text FROM pg_index " +
                "WHERE indexrelid='r16_iopt_idx'::regclass");
        assertNotNull(opt);
        assertTrue(opt.contains("1") && opt.contains("2"),
                "pg_index.indoption must encode column DESC/NULLS bits; got " + opt);
    }

