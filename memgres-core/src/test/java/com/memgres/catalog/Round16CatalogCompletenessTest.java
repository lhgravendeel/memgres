    // =========================================================================
    // J4. pg_attribute.attcompression
    // =========================================================================

    @Test
    void attcompression_defaults_to_p_or_empty_not_blank() throws SQLException {
        exec("DROP TABLE IF EXISTS r16_comp");
        exec("CREATE TABLE r16_comp (t text)");
        try (Statement s = conn.createStatement();
             ResultSet rs = s.executeQuery(
                     "SELECT attcompression::text FROM pg_attribute " +
                             "WHERE attrelid='r16_comp'::regclass AND attname='t'")) {
            assertTrue(rs.next());
            String v = rs.getString(1);
            // Default should be empty/unset (meaning \"use cluster default\"). But hardcoding \"\" unconditionally
            // hides user SET COMPRESSION. Accept NULL or \"\" here; subsequent test (J4b) exercises set.
            assertTrue(v == null || v.isEmpty() || "p".equals(v) || "l".equals(v),
                    "default attcompression must be empty/p/l; got '" + v + "'");
        }
        exec("ALTER TABLE r16_comp ALTER COLUMN t SET COMPRESSION pglz");
        String after = str("SELECT attcompression::text FROM pg_attribute " +
                "WHERE attrelid='r16_comp'::regclass AND attname='t'");
        assertEquals("p", after,
                "after SET COMPRESSION pglz, attcompression must be 'p'; got '" + after + "'");
    }

