    static Memgres memgres;
    static Connection conn;

    @BeforeAll
    static void setUp() throws Exception {
        memgres = Memgres.builder().port(0).build().start();
        conn = DriverManager.getConnection(
                memgres.getJdbcUrl() + "?preferQueryMode=simple",
                memgres.getUser(), memgres.getPassword());
        conn.setAutoCommit(true);
    }

    @AfterAll
    static void tearDown() throws Exception {
        if (conn != null) conn.close();
        if (memgres != null) memgres.close();
    }

    private static void exec(String sql) throws SQLException {
        try (Statement s = conn.createStatement()) { s.execute(sql); }
    }

    private static String str(String sql) throws SQLException {
        try (Statement s = conn.createStatement(); ResultSet rs = s.executeQuery(sql)) {
            assertTrue(rs.next());
            return rs.getString(1);
        }
    }

    private static int intQ(String sql) throws SQLException {
        try (Statement s = conn.createStatement(); ResultSet rs = s.executeQuery(sql)) {
            assertTrue(rs.next());
            return rs.getInt(1);
        }
    }

    // =========================================================================
    // J1. pg_class.relkind encodes 'm' for matviews, 'I' for partitioned index
    // =========================================================================

    @Test
    void matview_relkind_is_m() throws SQLException {
        exec("DROP MATERIALIZED VIEW IF EXISTS r16_mv");
        exec("CREATE MATERIALIZED VIEW r16_mv AS SELECT 1 AS v");
        String k = str("SELECT relkind::text FROM pg_class WHERE relname='r16_mv'");
        assertEquals("m", k,
                "materialized view must have pg_class.relkind='m'; got '" + k + "'");
    }

    @Test
    void partitioned_index_relkind_is_capital_I() throws SQLException {
        exec("DROP TABLE IF EXISTS r16_pi CASCADE");
        exec("CREATE TABLE r16_pi (id int) PARTITION BY RANGE (id)");
        exec("CREATE INDEX r16_pi_idx ON r16_pi(id)");
        String k = str("SELECT relkind::text FROM pg_class WHERE relname='r16_pi_idx'");
        assertEquals("I", k,
                "partitioned index must have pg_class.relkind='I' (uppercase); got '" + k + "'");
    }

