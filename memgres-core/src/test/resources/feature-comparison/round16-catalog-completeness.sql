-- ============================================================================
-- Feature Comparison: Round 16 — Catalog completeness
-- Target: PostgreSQL 18 vs Memgres
-- ============================================================================

DROP SCHEMA IF EXISTS r16_cat CASCADE;
CREATE SCHEMA r16_cat;
SET search_path = r16_cat, public;

-- ============================================================================
-- SECTION J1: pg_class.relkind
-- ============================================================================

CREATE MATERIALIZED VIEW mv AS SELECT 1 AS v;

-- 1. Matview relkind = 'm'
-- begin-expected
-- columns: k
-- row: m
-- end-expected
SELECT relkind::text AS k FROM pg_class WHERE relname='mv'
  AND relnamespace='r16_cat'::regnamespace;

CREATE TABLE pi (id int) PARTITION BY RANGE (id);
CREATE INDEX pi_idx ON pi(id);

-- 2. Partitioned index relkind = 'I'
-- begin-expected
-- columns: k
-- row: I
-- end-expected
SELECT relkind::text AS k FROM pg_class WHERE relname='pi_idx'
  AND relnamespace='r16_cat'::regnamespace;

-- ============================================================================
-- SECTION J2: pg_class.reloptions
-- ============================================================================

CREATE TABLE ro (id int) WITH (fillfactor=70);

-- 3. reloptions contains fillfactor=70
