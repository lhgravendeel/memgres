-- ============================================================================
-- SECTION J9: pg_aggregate built-ins present
-- ============================================================================

-- 12. count() aggregate in pg_aggregate
-- begin-expected
-- columns: ok
-- row: t
-- end-expected
SELECT (count(*) >= 1) AS ok FROM pg_aggregate a
JOIN pg_proc p ON a.aggfnoid=p.oid WHERE p.proname='count';

-- 13. array_agg in pg_aggregate
-- begin-expected
-- columns: ok
-- row: t
-- end-expected
SELECT (count(*) >= 1) AS ok FROM pg_aggregate a
JOIN pg_proc p ON a.aggfnoid=p.oid WHERE p.proname='array_agg';

-- ============================================================================
-- SECTION J10: pg_operator.oprcode wired for built-ins
-- ============================================================================

-- 14. int4=int4 has non-zero oprcode
