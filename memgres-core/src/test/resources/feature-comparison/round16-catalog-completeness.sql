WHERE conrelid='r16_cat.cm_c'::regclass AND contype='f';

-- ============================================================================
-- SECTION J12: pg_index.indoption + indkey type
-- ============================================================================

CREATE TABLE iopt (a int, b int);
CREATE INDEX iopt_idx ON iopt (a DESC NULLS LAST, b ASC NULLS FIRST);

-- 16. indoption contains per-column bits (1 for a, 2 for b)
-- begin-expected
-- columns: ok
-- row: t
-- end-expected
SELECT (indoption::text LIKE '%1%' AND indoption::text LIKE '%2%') AS ok
FROM pg_index WHERE indexrelid='r16_cat.iopt_idx'::regclass;

-- 17. pg_index.indkey type is int2vector
-- begin-expected
-- columns: t
-- row: int2vector
-- end-expected
SELECT format_type(atttypid, atttypmod) AS t FROM pg_attribute
WHERE attrelid='pg_index'::regclass AND attname='indkey';

-- ============================================================================
-- SECTION J13: pg_range for user range type
